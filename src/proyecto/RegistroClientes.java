    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField3.getText();
        
        if (name.length() < 3) {
            Handler.showMessage("Nombre muy corto", "Nombre invalido", Handler.ERROR);
            jTextField3.setText("");
            return;
        }
        
        String firstSurName = jTextField4.getText();
        if (firstSurName.length() < 3) {
            Handler.showMessage("Primer apellido muy corto", "Primer apellido invalido", Handler.ERROR);
            jTextField4.setText("");
            return;
        }
        
        String secondSurName = jTextField5.getText();
        if (secondSurName.length() < 3) {
            Handler.showMessage("Segundo apellido muy corto", "Segundo apellido invalido", Handler.ERROR);
            jTextField5.setText("");
            return;
        }
        
        
        String username = jTextField1.getText();
        String password = jTextField2.getText();
        
        if (password.length() < 8) {
            Handler.showMessage("Contraseña muy corta", "Contraseña invalida", Handler.ERROR);
            jTextField2.setText("");
            return;
        }
        
        boolean state = jCheckBox1.isSelected();
        String phone = jTextField7.getText();
        if (password.length() < 8) {
            Handler.showMessage("Número de teléfono muy corto", "Número de teléfono invalido", Handler.ERROR);
            jTextField7.setText("");
            return;
        }
        
        String identification = jTextField6.getText();
        if (identification.length() < 8) {
            Handler.showMessage("Identificacion invalida", "Identificación invalida", Handler.ERROR);
            jTextField6.setText("");
            return;
        }
        long id = Long.parseLong(identification);
        
        listaSimple.add(new Cliente(name, firstSurName, secondSurName, id, username, password, state, phone));
        this.clear();
    }//GEN-LAST:event_jButton1ActionPerformed

