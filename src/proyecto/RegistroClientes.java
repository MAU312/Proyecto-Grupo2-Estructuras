    public void checkData() {
        String identification = "";
        do {
            identification = Handler.inputString("Digite la identificación del cliente").trim();
            if (identification == null) identification = "";
        } while (identification.length() < 8);
        
        Cliente client = listaSimple.find(Long.parseLong(identification));
        if (client == null) {
            Handler.showMessage("El cliente no existe", "Error: Not Found", Handler.ERROR);
            this.dispose();
            
            client = new Cliente("", "", "", 0L, "", "", false, "");
        }
        
        jTextField1.setText(client.getUsername());
        jTextField2.setText(client.getPassword());
        jTextField3.setText(client.getName());
        jTextField4.setText(client.getFirstSurName());
        jTextField5.setText(client.getSecondSurName());
        jTextField6.setText(String.valueOf(client.getIdentification()));
        jTextField7.setText(client.getPhone());
        jCheckBox1.setSelected(client.isState());
    }
    
